// Encapsulation using private modifier

public class Employee {
    
    private int ID;
    private String name;

    public void setId(int ID) {
        this.ID = ID;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getID() {
        return ID;
    }

    public String setName() {
        return name;
    }
    
}